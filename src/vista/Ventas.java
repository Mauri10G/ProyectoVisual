    private void jTxtCodeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTxtCodeActionPerformed
        // TODO add your handling code here:
        if (!"".equals(jTxtCode.getText())) {
            dao.ProductoDAO daoProd = new dao.ProductoDAO();
            modelo.Producto producto = daoProd.buscarPorCodigo(jTxtCode.getText());

            
        if (producto != null) {
                productoTemporal = producto; 
                jTxtNombrePro.setText(producto.getNombre());
                jSpnCantidad.setValue(1); 
                jSpnCantidad.requestFocus(); 
            }else {
                javax.swing.JOptionPane.showMessageDialog(null, "Producto no encontrado o código incorrecto");
                jTxtNombrePro.setText("");
                jTxtCode.requestFocus(); 
            }
        }
        
    }//GEN-LAST:event_jTxtCodeActionPerformed

    private void jBtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAgregarActionPerformed
        // TODO add your handling code here:
        if (productoTemporal != null) {
            int cantidad = Integer.parseInt(jSpnCantidad.getValue().toString());

            if (cantidad > 0) {
                if (cantidad <= productoTemporal.getStock()) {
                    double subtotalItem = cantidad * productoTemporal.getPrecio();

                    Object[] fila = new Object[6]; 
                    fila[0] = productoTemporal.getIdProducto(); 
                    fila[1] = productoTemporal.getCodigo();
                    fila[2] = productoTemporal.getNombre();
                    fila[3] = productoTemporal.getPrecio();
                    fila[4] = cantidad;
                    fila[5] = subtotalItem;
                    tablita.addRow(fila);

                    calcularTotales();

                    jTxtCode.setText("");
                    jTxtNombrePro.setText("");
                    jSpnCantidad.setValue(0);
                    productoTemporal = null;
                    jTxtCode.requestFocus();

                } else {
                    javax.swing.JOptionPane.showMessageDialog(null, "Stock insuficiente. Stock actual: " + productoTemporal.getStock());
                }
            } else {
                javax.swing.JOptionPane.showMessageDialog(null, "La cantidad debe ser mayor a 0");
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Primero busca un producto presionando Enter en el código");
        }
    }//GEN-LAST:event_jBtnAgregarActionPerformed

    private void jBtnQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnQuitarActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = jTblDetalleVenta.getSelectedRow();
        if (filaSeleccionada >= 0) {
            tablita.removeRow(filaSeleccionada);
            calcularTotales(); // Recalculamos el dinero al quitar la fila
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Selecciona un producto de la tabla para quitarlo");
        }
    }//GEN-LAST:event_jBtnQuitarActionPerformed

    private void jBtnGenerarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGenerarVentaActionPerformed
        // TODO add your handling code here:
        if (jTblDetalleVenta.getRowCount() == 0) {
            javax.swing.JOptionPane.showMessageDialog(null, "El carrito está vacío. Agregue productos.");
            return;
        }

        modelo.Factura factura = new modelo.Factura();
        factura.setNumeroFactura(jLblNumero.getText().replace("Factura Nro: ", "")); 
        factura.setIdCliente(idClienteFactura);
        factura.setIdUsuario(this.idCajeroActivo); 
        factura.setSubtotal(subtotalVenta);
        factura.setPorcentajeIva(15.00); 
        factura.setTotalIva(ivaVenta);
        factura.setTotal(totalVenta);

        java.util.List<modelo.DetalleFactura> lista = new java.util.ArrayList<>();

        for (int i = 0; i < jTblDetalleVenta.getRowCount(); i++) {
            modelo.DetalleFactura detalle = new modelo.DetalleFactura();

            detalle.setIdProducto(Integer.parseInt(jTblDetalleVenta.getValueAt(i, 0).toString()));
            detalle.setPrecioUnitario(Double.parseDouble(jTblDetalleVenta.getValueAt(i, 3).toString()));
            detalle.setCantidad(Integer.parseInt(jTblDetalleVenta.getValueAt(i, 4).toString()));
            detalle.setSubtotalItem(Double.parseDouble(jTblDetalleVenta.getValueAt(i, 5).toString()));

            lista.add(detalle);
        }

        dao.FacturaDAO daoFac = new dao.FacturaDAO();
        if (daoFac.registrarVenta(factura, lista)) {
            javax.swing.JOptionPane.showMessageDialog(null, "¡Venta Registrada Exitosamente!");
            try {
                java.sql.Connection con = modelo.Conexion.conectar();

                java.io.InputStream rutaReporte = getClass().getResourceAsStream("/reportes/Factura.jasper");

                java.util.Map<String, Object> parametros = new java.util.HashMap<>();
                parametros.put("NUM_FACTURA", factura.getNumeroFactura()); 

                net.sf.jasperreports.engine.JasperPrint jp = net.sf.jasperreports.engine.JasperFillManager.fillReport(rutaReporte, parametros, con);

                net.sf.jasperreports.view.JasperViewer.viewReport(jp, false);

            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(null, "Error al generar la factura: " + e.getMessage());
            }
        

            tablita.setRowCount(0); 
            calcularTotales(); 
            jTxtCedula.setText("");
            jTxtNombre.setText("");
            idClienteFactura = 0; 
            cargarNumeroFactura(); 
        }
    }//GEN-LAST:event_jBtnGenerarVentaActionPerformed

    private void jTxtCedulaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTxtCedulaActionPerformed
        // TODO add your handling code here:
        String cedula = jTxtCedula.getText().trim();

        if (!cedula.isEmpty()) {
            dao.ClienteDAO daoCli = new dao.ClienteDAO();
            modelo.Cliente clienteEncontrado = daoCli.buscarPorCedula(cedula);

            if (clienteEncontrado != null) {
                jTxtNombre.setText(clienteEncontrado.getNombre());
                idClienteFactura = clienteEncontrado.getIdCliente();
                jTxtCode.requestFocus();
            } else {
                javax.swing.JOptionPane.showMessageDialog(null, "Cliente no encontrado. Se facturará como Consumidor Final.");
                jTxtNombre.setText("Consumidor Final");
                idClienteFactura = 0;
                jTxtCode.requestFocus();
            }
        } else {
            jTxtNombre.setText("Consumidor Final");
            idClienteFactura = 0;
            jTxtCode.requestFocus();
        }
    }//GEN-LAST:event_jTxtCedulaActionPerformed

