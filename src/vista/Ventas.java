    public void cargarTabla(){
        tablita.addColumn("ID");
        tablita.addColumn("Código");
        tablita.addColumn("Nombre");
        tablita.addColumn("Precio");
        tablita.addColumn("Cantidad");
        tablita.addColumn("Subtotal");
        jTblDetalleVenta.setModel(tablita);
    }
    
    public void calcularTotales() {
        subtotalVenta = 0.0;
        for (int i = 0; i < jTblDetalleVenta.getRowCount(); i++) {
            subtotalVenta += Double.parseDouble(jTblDetalleVenta.getValueAt(i, 5).toString()); 
        }

        ivaVenta = subtotalVenta * 0.15;
        totalVenta = subtotalVenta + ivaVenta;

        jTxtSubtotal.setText(String.format("%.2f", subtotalVenta));
        jTxtIVA.setText(String.format("%.2f", ivaVenta));
        jTxtTotal.setText(String.format("%.2f", totalVenta));
    }
    
    public void cargarNumeroFactura() {
        dao.FacturaDAO daoFac = new dao.FacturaDAO();
        String num = daoFac.generarNumeroFactura();
        jLblNumero.setText("Factura Nro: " + num);
    }

