    private void jBtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGuardarActionPerformed
        // TODO add your handling code here:
        if (!"".equals(jTxtCode.getText()) && !"".equals(jTxtName.getText()) && 
            !"".equals(jTxtPrecio.getText()) && !"".equals(jTxtStock.getText())) {

            producto.setCodigo(jTxtCode.getText());
            producto.setNombre(jTxtName.getText());
            producto.setPrecio(Double.parseDouble(jTxtPrecio.getText()));
            producto.setStock(Integer.parseInt(jTxtStock.getText()));

            if (prodDao.insertar(producto)) {
                javax.swing.JOptionPane.showMessageDialog(null, "Producto Guardado");
                limpiarCampos();
                listarProductos();
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Por favor, llena todos los campos");
        }
    }//GEN-LAST:event_jBtnGuardarActionPerformed

    private void jTblCrudMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTblCrudMouseClicked
        // TODO add your handling code here:
        int fila = jTblCrud.rowAtPoint(evt.getPoint());
        if (fila > -1) {
            // Guardamos el código original en nuestra variable global
            codigoSeleccionado = jTblCrud.getValueAt(fila, 0).toString();

            // Pasamos los datos a las cajitas de texto
            jTxtCode.setText(codigoSeleccionado);
            jTxtName.setText(jTblCrud.getValueAt(fila, 1).toString());
            jTxtPrecio.setText(jTblCrud.getValueAt(fila, 2).toString());
            jTxtStock.setText(jTblCrud.getValueAt(fila, 3).toString());
        }
    }//GEN-LAST:event_jTblCrudMouseClicked

    private void jBtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEditarActionPerformed
        // TODO add your handling code here:
        if (codigoSeleccionado.equals("")) {
            javax.swing.JOptionPane.showMessageDialog(null, "Selecciona una fila en la tabla primero");
        } else {
            producto.setCodigo(jTxtCode.getText());
            producto.setNombre(jTxtName.getText());
            producto.setPrecio(Double.parseDouble(jTxtPrecio.getText()));
            producto.setStock(Integer.parseInt(jTxtStock.getText()));

            if (prodDao.actualizarPorCodigo(producto, codigoSeleccionado)) {
                javax.swing.JOptionPane.showMessageDialog(null, "Producto Actualizado");
                limpiarCampos();
                listarProductos();
                codigoSeleccionado = "";
            }
        }
    }//GEN-LAST:event_jBtnEditarActionPerformed

    private void jBtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEliminarActionPerformed
        // TODO add your handling code here:
        if (codigoSeleccionado.equals("")) {
                javax.swing.JOptionPane.showMessageDialog(null, "Selecciona una fila en la tabla primero");
            } else {
                int pregunta = javax.swing.JOptionPane.showConfirmDialog(null, "¿Estás seguro de eliminar este producto?");
                if (pregunta == 0) { 
                    if (prodDao.eliminarPorCodigo(codigoSeleccionado)) {
                        javax.swing.JOptionPane.showMessageDialog(null, "Producto Eliminado");
                        limpiarCampos(); 
                        listarProductos(); 
                        codigoSeleccionado = ""; 
                    }
                }
            }
    }//GEN-LAST:event_jBtnEliminarActionPerformed

