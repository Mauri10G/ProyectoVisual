    public void cargartabla(){
        jTblCrud.setModel(tablita);
        tablita.addColumn("Código");
        tablita.addColumn("Nombre");
        tablita.addColumn("Precio");
        tablita.addColumn("Stock");
    
    }
    
    public void listarProductos() {
        try {
            tablita.setRowCount(0);
            Connection con = Conexion.conectar();
            
            String sql = "SELECT * FROM productos";
            String Fila[] = new String[4];
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            while (rs.next()){
                Fila[0] = rs.getString("codigo");
                Fila[1] = rs.getString("nombre");
                Fila[2] = rs.getString("precio");
                Fila[3] = rs.getString("stock");
                
                tablita.addRow(Fila);
                
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "No se pudo cargar los productos");
        }

    }

