    private void jBtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGuardarActionPerformed
        // TODO add your handling code here:
        if (!"".equals(jTxtCedula.getText()) && !"".equals(jTxtNombre.getText())) {

            cliente.setIdentificacion(jTxtCedula.getText());
            cliente.setNombre(jTxtNombre.getText());
            cliente.setCorreo(jTxtCorreo.getText());
            cliente.setTelefono(jTxtCelular.getText());

            if (cliDao.insertar(cliente)) {
                javax.swing.JOptionPane.showMessageDialog(null, "Cliente Registrado");
                limpiarCampos();
                listarClientes();
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "La cédula y el nombre son obligatorios");
        }
    }//GEN-LAST:event_jBtnGuardarActionPerformed

    private void jTblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTblClientesMouseClicked
        // TODO add your handling code here:
        int fila = jTblClientes.rowAtPoint(evt.getPoint());
        if (fila > -1) {
            Object valorCelda = jTblClientes.getValueAt(fila, 0);

            if (valorCelda != null) {
                cedulaSeleccionada = valorCelda.toString();

                jTxtCedula.setText(cedulaSeleccionada);
                jTxtNombre.setText(jTblClientes.getValueAt(fila, 1).toString());
                jTxtCorreo.setText(jTblClientes.getValueAt(fila, 2).toString());
                jTxtCelular.setText(jTblClientes.getValueAt(fila, 3).toString());
            }
        }
    }//GEN-LAST:event_jTblClientesMouseClicked

    private void jBtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEditarActionPerformed
        // TODO add your handling code here:
        if (cedulaSeleccionada.equals("")) {
            javax.swing.JOptionPane.showMessageDialog(null, "Selecciona un cliente en la tabla primero");
        } else {
            cliente.setIdentificacion(jTxtCedula.getText());
            cliente.setNombre(jTxtNombre.getText());
            cliente.setCorreo(jTxtCorreo.getText());
            cliente.setTelefono(jTxtCelular.getText());

            if (cliDao.actualizarPorCedula(cliente, cedulaSeleccionada)) {
                javax.swing.JOptionPane.showMessageDialog(null, "Cliente Actualizado");
                limpiarCampos();
                listarClientes();
                cedulaSeleccionada = "";
            }
        }
    }//GEN-LAST:event_jBtnEditarActionPerformed

    private void jBtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEliminarActionPerformed
        // TODO add your handling code here:
        if (cedulaSeleccionada.equals("")) {
            javax.swing.JOptionPane.showMessageDialog(null, "Selecciona un cliente en la tabla primero");
        } else {
            int pregunta = javax.swing.JOptionPane.showConfirmDialog(null, "¿Estás seguro de eliminar este cliente?");
            if (pregunta == 0) { 
                if (cliDao.eliminarPorCedula(cedulaSeleccionada)) {
                    javax.swing.JOptionPane.showMessageDialog(null, "Cliente Eliminado");
                    limpiarCampos();
                    listarClientes();
                    cedulaSeleccionada = "";
                }
            }
        }
    }//GEN-LAST:event_jBtnEliminarActionPerformed

