    public void cargartabla() {
        jTblClientes.setModel(tablitaClientes);
        tablitaClientes.addColumn("Cédula");
        tablitaClientes.addColumn("Nombre");
        tablitaClientes.addColumn("Correo");
        tablitaClientes.addColumn("Teléfono");
    }

    public void listarClientes() {
        try {
            tablitaClientes.setRowCount(0); 
            java.sql.Connection con = modelo.Conexion.conectar();
            
            String sql = "SELECT identificacion, nombre, correo, telefono FROM clientes ORDER BY id_cliente ASC";
            String Fila[] = new String[4];
            
            java.sql.Statement st = con.createStatement();
            java.sql.ResultSet rs = st.executeQuery(sql);
            
            while (rs.next()) {
                Fila[0] = rs.getString("identificacion");
                Fila[1] = rs.getString("nombre");
                Fila[2] = rs.getString("correo");
                Fila[3] = rs.getString("telefono");
                
                tablitaClientes.addRow(Fila);
            }
        } catch (java.sql.SQLException ex) {
            javax.swing.JOptionPane.showMessageDialog(rootPane, "No se pudo cargar los clientes: " + ex.getMessage());
        }
    }

