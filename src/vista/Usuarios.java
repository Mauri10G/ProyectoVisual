    public void cargarRoles() {
        jCbxRol.setModel(combito);
        jCbxRol.removeAllItems(); 
        java.util.List<String> roles = usrDao.consultarRoles();
        for (String rol : roles) {
            jCbxRol.addItem(rol);
        }
    }

    public void listarUsuarios() {
        try {
            tablitaUsuarios.setRowCount(0); 
            Connection con = Conexion.conectar();
            String sql = "SELECT u.username, u.password_hash, r.nombre AS nombre_rol " + "FROM usuarios u INNER JOIN roles r ON u.id_rol = r.id_rol";

            String Fila[] = new String[3];

            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                Fila[0] = rs.getString("username");
                Fila[1] = "********";
                Fila[2] = rs.getString("nombre_rol"); 

                tablitaUsuarios.addRow(Fila);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "No se pudo cargar los usuarios: " + ex.getMessage());
        }
    }
    public void cargartabla() {
        jTblUsuarios.setModel(tablitaUsuarios);
        tablitaUsuarios.addColumn("Usuario");
        tablitaUsuarios.addColumn("Contraseña");
        tablitaUsuarios.addColumn("Rol");
    }

    public void limpiarCampos() {
        jTxtUser.setText("");
        jPsfContra.setText("");
        if(jCbxRol.getItemCount() > 0) {
            jCbxRol.setSelectedIndex(0);
        }
        userSeleccionado = "";
    }

