    private void jBtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGuardarActionPerformed
        // TODO add your handling code here:
        if (!"".equals(jTxtUser.getText()) && jPsfContra.getPassword().length > 0) {

            usuario.setUsername(jTxtUser.getText());
            usuario.setPassword(new String(jPsfContra.getPassword()));
            usuario.setIdRol(jCbxRol.getSelectedIndex() + 1); 
            usuario.setActivo(true);

            if (usrDao.insertar(usuario)) {
                javax.swing.JOptionPane.showMessageDialog(null, "Usuario Guardado exitosamente");
                limpiarCampos();
                listarUsuarios();
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Por favor, llena todos los campos");
        }
    }//GEN-LAST:event_jBtnGuardarActionPerformed

    private void jTblUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTblUsuariosMouseClicked
        // TODO add your handling code here:
        int fila = jTblUsuarios.rowAtPoint(evt.getPoint());

            if (fila > -1) {
                Object valorCelda = jTblUsuarios.getValueAt(fila, 0);

                if (valorCelda != null) {
                    userSeleccionado = valorCelda.toString();

                    jTxtUser.setText(userSeleccionado);
                    jPsfContra.setText(""); 

                    Object valorRol = jTblUsuarios.getValueAt(fila, 2);
                    if (valorRol != null) {
                        jCbxRol.setSelectedItem(valorRol.toString());
                    }
                }
            }
    }//GEN-LAST:event_jTblUsuariosMouseClicked

    private void jBtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEditarActionPerformed
        // TODO add your handling code here:
        if (userSeleccionado.equals("")) {
            javax.swing.JOptionPane.showMessageDialog(null, "Selecciona una fila en la tabla primero");
        } else {
            usuario.setUsername(jTxtUser.getText());
            usuario.setPassword(new String(jPsfContra.getPassword()));
            usuario.setIdRol(jCbxRol.getSelectedIndex() + 1);
            usuario.setActivo(true);

            if (usrDao.actualizarPorUsername(usuario, userSeleccionado)) {
                javax.swing.JOptionPane.showMessageDialog(null, "Usuario Actualizado exitosamente");
                limpiarCampos();
                listarUsuarios();
                userSeleccionado = "";
            }
    }
    }//GEN-LAST:event_jBtnEditarActionPerformed

    private void jBtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEliminarActionPerformed
        // TODO add your handling code here:
        if (userSeleccionado.equals("")) {
            javax.swing.JOptionPane.showMessageDialog(null, "Selecciona una fila en la tabla primero");
        } else {
            int pregunta = javax.swing.JOptionPane.showConfirmDialog(null, "¿Estás seguro de eliminar a este usuario?");
            if (pregunta == 0) { 
                if (usrDao.eliminarPorUsername(userSeleccionado)) {
                    javax.swing.JOptionPane.showMessageDialog(null, "Usuario Eliminado");
                    limpiarCampos();
                    listarUsuarios();
                    userSeleccionado = "";
                }
            }
        }
    }//GEN-LAST:event_jBtnEliminarActionPerformed

